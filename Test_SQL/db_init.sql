DROP TABLE IF EXISTS students;
DROP TABLE IF EXISTS visit;
DROP TABLE IF EXISTS progress_303;
DROP TABLE IF EXISTS progress_402;

CREATE TABLE students
(
    id integer primary key,
    last_name varchar(64),
    initial varchar(1),
    group_number integer
);


CREATE TABLE visit (        
    student_id integer,
    date date,
    foreign key (student_id) references students(id));

CREATE TABLE date_progress (        
    id integer primary key,
    date date);


CREATE TABLE progress_303
(
    id_student integer,
    id_date integer,
    task_number integer,
    score real,
    foreign key (id_date) references date_progress(id),
    foreign key (id_student) references students(id)
);

CREATE TABLE progress_402
(
    id_student integer,
    task_number integer,
    score real,
    foreign key (id_date) references date_progress(id),
    foreign key (id_student) references students(id)
);

